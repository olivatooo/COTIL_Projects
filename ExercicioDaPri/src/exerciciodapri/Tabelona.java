    public void listar(String tabela)
    {
    DefaultTableModel dfm = (DefaultTableModel)tabela_show.getModel();
    dados d=new dados();
    d.conecta("143.106.241.1","3infd1","3infd1","3infd1");
    ResultSet rs=d.consulta("SELECT * FROM "+tabela);
    ResultSet qnts = d.consulta("SELECT * FROM estoque");
    
    try
    {
    int linha=0;
    while((rs.next()&&qnts.next()))
        {
        dfm.addRow(new String[dfm.getColumnCount()]);
        dfm.setValueAt(rs.getString("codigo"),linha,0);
        dfm.setValueAt(rs.getString("descricao"),linha,1);
        dfm.setValueAt(qnts.getString("quantidade"),linha,2);
        linha++;
        }
    }catch(Exception e)
        {
        
        }
    
    }
