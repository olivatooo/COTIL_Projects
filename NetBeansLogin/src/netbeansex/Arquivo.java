    public void carregaImagem(JButton botao, String arquivo)
    {
    try{
        File file =new File(arquivo);
        BufferedImage bufi= ImageIO.read(file);
        ImageIcon ico=new ImageIcon(bufi);
        
        Image img = ico.getImage() ;  
        Image newimg = img.getScaledInstance( imagem.getWidth(),imagem.getHeight(),  java.awt.Image.SCALE_SMOOTH ) ;  
        ico = new ImageIcon( newimg );
        botao.setIcon(ico);
        
        //BIANCA QND ESTUDAR IGNORE ISSO
       // botao.setIcon(new ImageIcon(ImageIO.read(getClass().getResource(arquivo))));
       
        }catch(Exception e)
            {
                System.out.println("ERRO:"+e.toString());
            }
    }
    public ImageIcon Resizer(ImageIcon ico)
    {
        Image img = ico.getImage() ;  
        Image newimg = img.getScaledInstance( imagem.getWidth(),imagem.getHeight(),  java.awt.Image.SCALE_SMOOTH ) ;  
        ico = new ImageIcon( newimg );
        return ico;
    }

