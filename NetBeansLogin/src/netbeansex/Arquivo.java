    private void pesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pesquisarMouseClicked
        Dados d = new Dados();
        d.conecta("143.106.241.1","3infd1", "3infd1", "3infd1");
        ResultSet retorno=d.consulta("SELECT * FROM cadastro WHERE nome='"+jTextField2.getText()+"'");
        try
        {
            while(retorno.next())
            {
                jTextField2.setText(retorno.getString("nome"));
                jTextField3.setText(retorno.getString("arquivo"));
                java.sql.Blob blob = retorno.getBlob("imagem");               
                ImageIcon img =new ImageIcon(blob.getBytes(1,(int)blob.length()));
                imagem.setIcon(Resizer(img));
                
            }
                   
        }catch (Exception e)
                    {
                    System.out.println("Erro-"+e);
                    }
    }//GEN-LAST:event_pesquisarMouseClicked

